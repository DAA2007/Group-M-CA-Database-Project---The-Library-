# 2 - Create the neccessary tables
CREATE TABLE Books(
	BookID int auto_increment primary key,
    Title VARCHAR(50),
    Genre VARCHAR(25),
    Author VARCHAR(100),
    Publisher VARCHAR(35),
    DateOfPublishing date,
    IsBorrowed boolean,
    UNIQUE INDEX(BookID, Title)
);

CREATE TABLE Borrows(
	BookID int auto_increment primary key,
    Title VARCHAR(50),
    DateOfBorrow date,
    BorrowerID int,
    ReturnTime date,
    IsOverdue boolean,
    foreign key (BookID, Title)
    references Books(BookID, Title),
    #This one works fine, weirdly.
    UNIQUE INDEX(BorrowerID)
